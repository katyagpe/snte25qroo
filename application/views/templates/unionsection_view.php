                            <h1>¿Quienes somos?</h1>
                            <p>
                                Somos una organización sindical orgullosamente arraigados a una historia de casi ocho décadas al servicio de la educación de Quintana Roo y de México, cuyo objetivo es la defensa y continuo mejoramiento de las condiciones económicas, sociales, laborales y profesionales de sus agremiados.
                                <br>
                                Nacimos un día viernes 25 de Agosto del año de 1944 en la ciudad de Mérida Yucatán, en virtud de las extraordinarias privaciones que se vivían en lo que era el territorio federal de Quintana Roo y ello propiciaba que fuera más fácil reunirse y encontrarse en la capital Yucateca en vez de hacerlo en Felipe Carrillo Puerto o ciudad Chetumal.
                                <br>
                                Casi todos los maestros vivíamos con nuestras familias incluyendo a los hijos, en nuestras remotas e incomunicadas comunidades por meses y prácticamente sólo podíamos vernos en los períodos vacacionales y era entonces cuando hacíamos reuniones en las que evaluábamos las condiciones que nos tocaba experimentar.
                                <br>
                                Carecíamos de salarios dignos, de seguridad en el empleo, de servicios asistenciales y médicos, de arbitrariedades en el trato, en la movilización para tener mejores lugares de adscripción; prácticamente sin condiciones profesionales y de vida, en tanto que además de la labor en las aulas, realizábamos otras actividades de asesoramiento hacia las cooperativas, sindicatos, centrales y organizaciones campesinas, organizábamos a los chicleros, a los madereros, encabezando luchas obreras, enseñando a leer y escribir a los adultos, impulsando una real educación socialista.
                                <br>
                                Salarios de noventa pesos mensuales -tres pesos diario- en aquellos tiempos que apenas alcanzaban para sobrevivir; en muchos casos, compañeros que fueron considerados espías del gobierno y que por ello eran hostigados, vivir con las familias en condiciones muy difíciles puesto que eran comunidades que prácticamente carecían de todo además de estar sujetos a cambios de adscripción a donde las autoridades administrativas así lo consideraban, era lo normal de ser maestro. 
                                <br>
                                Sin tener conocimiento pleno, problemas similares vivían nuestros compañeros profesores en cada rincón de nuestra patria, quienes también buscaban formas que les permitieran tener una vida en la que desarrollaran a plenitud la labor educativa.
